import 'package:flutter/material.dart';

/// Identifiers for supported custom icons.
///
/// The included icons are manually picked for GitHub tracker and
/// the font file was generated using https://www.fluttericon.com.
///
/// The identifiers mimic the [Icons] class.
class CustomIcons {
  CustomIcons._();

  /// The GitHub mark.
  static const github = IconData(0xe801, fontFamily: 'CustomIcons');

  /// The Twitter icon.
  static const twitter = IconData(0xe802, fontFamily: 'CustomIcons');
}
